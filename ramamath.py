def VolKubus (num):
    return num ** 3

def KelKubus (num):
    return num * 12

def LuasKubus (num):
    return 6 * (num * num)

def LuasLingkaran (num):
    return 22/7 * num

def DiameterLingkaran (num):
    return 2 * num

def LuasPersegiPanjang (p,l):
    return p * l

def KelPersegiPanjang (p,l):
    return 2 * (p + l)

def Pertambahan (num1,num2):
    return num1 + num2

def Pengurangan (num1,num2):
    return num1 - num2

def Perkalian (num1,num2):
    return num1 * num2

def Pembagian (num1,num2):
    return num1 / num2

def LuasTrapesium (a,b,t):
    return 1/2 * (a + b) * t 

def LuasLayang (d1,d2):
    return 1/2 * d1 * d2